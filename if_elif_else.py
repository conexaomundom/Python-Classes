
idade = int(input("Digite sua idade: "))

if idade >= 18 and idade <= 80:
    print("Olha só! Você pode se embriagar a vontade!")
    print("Que maravilha!")

elif idade > 80:
    print("Já foi seu tempo")
    print("Melhor cuidar dessa saúde")
else:
    print("Que pena! Você vai ter que esperar.")
    print("Pode tomar o seu leite para dormir.")